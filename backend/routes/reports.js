const express = require('express');
const router = express.Router();
const multer = require('multer');
const { auth } = require('../middleware/auth');
const emailService = require('../services/emailService');

// Configure multer for handling file uploads
const storage = multer.memoryStorage();
const upload = multer({ 
  storage: storage,
  limits: {
    fileSize: 10 * 1024 * 1024 // 10MB limit
  },
  fileFilter: (req, file, cb) => {
    if (file.mimetype === 'application/pdf') {
      cb(null, true);
    } else {
      cb(new Error('Only PDF files are allowed'), false);
    }
  }
});

// Send PDF report via email
router.post('/send-pdf', auth, upload.single('pdfFile'), async (req, res) => {
  console.log('ðŸ“¨ Received PDF report request');
  console.log('ðŸ“„ File received:', !!req.file);
  console.log('ðŸ“‹ Metadata received:', !!req.body.reportMetadata);
  
  try {
    const { reportMetadata } = req.body;
    
    if (!req.file) {
      console.log('âŒ No PDF file provided');
      return res.status(400).json({
        success: false,
        message: 'PDF file is required'
      });
    }

    if (!reportMetadata) {
      console.log('âŒ No report metadata provided');
      return res.status(400).json({
        success: false,
        message: 'Report metadata is required'
      });
    }

    const metadata = JSON.parse(reportMetadata);
    const reportData = metadata.reportData || [];
    
    console.log('âœ… Parsed metadata, report data entries:', reportData.length);
    
    // Demo email address
    const demoEmail = 'itzrvm2337@gmail.com';
    
    console.log('ðŸ“§ Sending email to:', demoEmail);
    
    // Send PDF report
    const emailSent = await emailService.sendPDFReport(
      demoEmail,
      reportData,
      metadata,
      req.file.buffer
    );

    if (emailSent) {
      res.json({
        success: true,
        message: `PDF report sent successfully to ${demoEmail}`,
        timestamp: new Date().toISOString()
      });
    } else {
      res.status(500).json({
        success: false,
        message: 'Failed to send PDF report'
      });
    }

  } catch (error) {
    console.error('Error sending PDF report:', error);
    res.status(500).json({
      success: false,
      message: 'Internal server error while sending PDF report'
    });
  }
});

